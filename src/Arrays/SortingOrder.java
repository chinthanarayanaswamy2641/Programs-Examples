package Arrays;

public class SortingOrder {

	public static void main(String[] args) {
		int a[]= {98,23,45,64,65,31,99,58,35};
		int temp;
		for(int i=0;i<a.length;i++) {
			for(int j=i+1;j<a.length;j++) {
				if(a[i]>a[j]) {
					temp=a[i];
					a[i]=a[j];
					a[j]=temp;
				}
			}
			System.out.println(a[i]);
		}


	}

}
